	let p=data.publications[i];
	let authors=[];
	for(let j=0;j<p.authors.length;j++) authors.push(p.authors[j].t==0 ? `<b>${p.authors[j].lastName}</b>` : (p.authors[j].t==2 ? `<mark>${p.authors[j].lastName}</mark>` : p.authors[j].lastName));
